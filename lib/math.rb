def addition(num1, num2)
  sum = num2 + num1 
  puts sum
  sum
end

def subtraction(num1, num2)
  minus = num1 - num2
  puts minus
  minus
end

def division(num1, num2)
  dividend = num1 / num2
  puts dividend
  dividend
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
  product
end

def modulo(num1, num2)
  modulus = num1.modulo(num2)
  puts modulus
  modulus
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
  root
end